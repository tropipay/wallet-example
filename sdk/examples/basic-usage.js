/**
 * TropiPay SDK - Basic Usage Example
 * 
 * This example demonstrates basic SDK operations including authentication,
 * account management, and simple transfers. Perfect for getting started
 * with TropiPay integration.
 * 
 * To run this example:
 * 1. Set environment variables: TROPIPAY_CLIENT_ID, TROPIPAY_CLIENT_SECRET
 * 2. Run: node examples/basic-usage.js
 */

const TropiPaySDK = require('../src/index');

async function basicUsageExample() {
  try {
    // =========================================================================
    // 1. INITIALIZE SDK
    // =========================================================================
    
    console.log('üöÄ Initializing TropiPay SDK...');
    
    const sdk = new TropiPaySDK({
      clientId: process.env.TROPIPAY_CLIENT_ID || 'your_client_id',
      clientSecret: process.env.TROPIPAY_CLIENT_SECRET || 'your_client_secret',
      environment: 'development', // 'development' uses https://sandbox.tropipay.me/api/v3
      debug: true // Enable detailed logging
    });
    
    // Listen to SDK events
    sdk.on('authenticated', (user) => {
      console.log('‚úÖ User authenticated:', user.profile.email);
    });
    
    sdk.on('error', (error) => {
      console.error('‚ùå SDK Error:', error.message);
    });
    
    // =========================================================================
    // 2. AUTHENTICATE
    // =========================================================================
    
    console.log('\nüìù Authenticating with TropiPay...');
    
    const authResult = await sdk.authenticate();
    
    console.log('‚úÖ Authentication successful!');
    console.log(`üë§ User: ${authResult.profile.firstName} ${authResult.profile.lastName}`);
    console.log(`üìß Email: ${authResult.profile.email}`);
    console.log(`üîê KYC Level: ${authResult.profile.kycLevel}`);
    console.log(`üì± 2FA Type: ${authResult.profile.twoFaType === 1 ? 'SMS' : 'Google Authenticator'}`);
    console.log(`üè¶ Available Accounts: ${authResult.accounts.length}`);
    console.log(`‚è∞ Token expires: ${authResult.expiresAt.toLocaleString()}`);
    
    // =========================================================================
    // 3. ACCOUNT MANAGEMENT
    // =========================================================================
    
    console.log('\nüí∞ Loading account information...');
    
    // Get all accounts
    const accounts = await sdk.accounts.getAll();
    
    console.log(`\nüìä Account Summary (${accounts.length} accounts):`);
    accounts.forEach(account => {
      console.log(`  ${account.currency} Account (${account.accountId}):`);
      console.log(`    üíµ Balance: $${account.balance.toFixed(2)}`);
      console.log(`    ‚úÖ Available: $${account.available.toFixed(2)}`);
      console.log(`    üîí Blocked: $${account.blocked.toFixed(2)}`);
      console.log(`    üìà Pending In: $${account.pendingIn.toFixed(2)}`);
      console.log(`    üìâ Pending Out: $${account.pendingOut.toFixed(2)}`);
      console.log(`    üìã Status: ${account.status}`);
      console.log(`    ‚≠ê Default: ${account.isDefault ? 'Yes' : 'No'}`);
      console.log('');
    });
    
    // Get USD accounts specifically
    const usdAccounts = await sdk.accounts.getByCurrency('USD');
    console.log(`üíµ USD Accounts: ${usdAccounts.length}`);
    
    // Get default account
    const defaultAccount = await sdk.accounts.getDefault('USD');
    if (defaultAccount) {
      console.log(`‚≠ê Default USD Account: ${defaultAccount.accountId} ($${defaultAccount.balance})`);
      
      // Check balance for potential transfer
      const balanceCheck = await sdk.accounts.checkBalance(defaultAccount.accountId, 10.00);
      console.log(`üí∏ Can transfer $10: ${balanceCheck.sufficient ? 'Yes' : 'No'}`);
      if (!balanceCheck.sufficient) {
        console.log(`   Need $${balanceCheck.shortfall.toFixed(2)} more`);
      }
    }
    
    // =========================================================================
    // 4. TRANSACTION HISTORY
    // =========================================================================
    
    console.log('\nüìú Loading recent transactions...');
    
    if (defaultAccount) {
      const movements = await sdk.accounts.getMovements(defaultAccount.accountId, {
        limit: 5 // Get last 5 transactions
      });
      
      console.log(`\nüìã Recent Transactions (${movements.length}):`);
      movements.forEach(movement => {
        const sign = movement.type.includes('IN') ? '+' : '-';
        console.log(`  ${movement.createdAt.toLocaleDateString()} - ${sign}$${movement.amount} (${movement.type})`);
        console.log(`    üìù ${movement.description}`);
        console.log(`    üè∑Ô∏è  ${movement.reference}`);
        console.log(`    üìä Status: ${movement.status}`);
        console.log(`    üí∞ Balance After: $${movement.balanceAfter}`);
        console.log('');
      });
      
      // Get account statistics
      const stats = await sdk.accounts.getStatistics(defaultAccount.accountId, {
        days: 30
      });
      
      console.log(`üìä 30-Day Account Statistics:`);
      console.log(`  üî¢ Transactions: ${stats.transactionCount}`);
      console.log(`  üì§ Total Sent: $${stats.totalSent.toFixed(2)}`);
      console.log(`  üì• Total Received: $${stats.totalReceived.toFixed(2)}`);
      console.log(`  üí≥ Total Fees: $${stats.totalFees.toFixed(2)}`);
      console.log(`  üìà Avg Transaction: $${stats.avgTransactionAmount.toFixed(2)}`);
    }
    
    // =========================================================================
    // 5. BENEFICIARY MANAGEMENT
    // =========================================================================
    
    console.log('\nüë• Loading beneficiaries...');
    
    const beneficiaries = await sdk.beneficiaries.getAll({
      limit: 10
    });
    
    console.log(`\nüìá Beneficiaries (${beneficiaries.length}):`);
    beneficiaries.forEach(beneficiary => {
      console.log(`  üë§ ${beneficiary.name} (${beneficiary.type})`);
      console.log(`    üè¶ Account: ${beneficiary.accountNumber}`);
      console.log(`    üí± Currency: ${beneficiary.currency}`);
      console.log(`    üåç Country: ${beneficiary.country}`);
      console.log(`    ‚úÖ Verified: ${beneficiary.isVerified ? 'Yes' : 'No'}`);
      
      if (beneficiary.type === 'EXTERNAL' && beneficiary.bankDetails?.swiftCode) {
        console.log(`    üèõÔ∏è  Bank: ${beneficiary.bankDetails.name}`);
        console.log(`    üîÄ SWIFT: ${beneficiary.bankDetails.swiftCode}`);
      }
      console.log('');
    });
    
    // Search beneficiaries
    if (beneficiaries.length > 0) {
      const searchResults = await sdk.beneficiaries.search('John');
      console.log(`üîç Search results for "John": ${searchResults.length} found`);
    }
    
    // =========================================================================
    // 6. DEMONSTRATION TRANSFER (SIMULATION ONLY)
    // =========================================================================
    
    console.log('\nüí∏ Demonstrating transfer simulation...');
    
    if (defaultAccount && beneficiaries.length > 0) {
      const firstBeneficiary = beneficiaries[0];
      
      try {
        console.log(`\nüéØ Simulating transfer to ${firstBeneficiary.name}...`);
        
        const simulation = await sdk.transfers.simulate({
          fromAccountId: defaultAccount.accountId,
          beneficiaryId: firstBeneficiary.id,
          amount: 5.00, // Small test amount
          reason: 'SDK Demo Transfer'
        });
        
        console.log('‚úÖ Transfer Simulation Results:');
        console.log(`  üíµ Amount to Pay: $${simulation.amountToPay.toFixed(2)}`);
        console.log(`  üí∞ Recipient Gets: ${simulation.toCurrency}${simulation.amountToReceive.toFixed(2)}`);
        console.log(`  üí≥ Total Fees: $${simulation.fees.toFixed(2)}`);
        console.log(`  üîÑ Exchange Rate: ${simulation.exchangeRate}`);
        console.log(`  üì± 2FA Required: ${simulation.requires2FA ? 'Yes' : 'No'}`);
        console.log(`  üí∞ Balance After: $${simulation.accountBalanceAfter.toFixed(2)}`);
        console.log(`  ‚è±Ô∏è  Estimated Arrival: ${simulation.estimatedArrival}`);
        
        if (simulation.breakdown) {
          console.log(`  üìã Fee Breakdown:`);
          console.log(`    üí∞ Base Fee: $${simulation.breakdown.baseFee.toFixed(2)}`);
          console.log(`    üîÑ Exchange Fee: $${simulation.breakdown.exchangeFee.toFixed(2)}`);
          console.log(`    üåç International Fee: $${simulation.breakdown.internationalFee.toFixed(2)}`);
        }
        
        // ‚ö†Ô∏è NOTE: We're NOT executing the transfer in this demo
        // To execute a real transfer, you would:
        //
        // if (simulation.requires2FA) {
        //   await sdk.transfers.requestSMSCode('+1234567890');
        //   const smsCode = '123456'; // Get from user input
        //   
        //   const result = await sdk.transfers.execute({
        //     fromAccountId: defaultAccount.accountId,
        //     beneficiaryId: firstBeneficiary.id,
        //     amount: 5.00,
        //     reason: 'SDK Demo Transfer',
        //     smsCode: smsCode
        //   });
        //   
        //   console.log('‚úÖ Transfer executed:', result.transferId);
        // }
        
      } catch (error) {
        console.log('‚ÑπÔ∏è  Transfer simulation failed (this is normal for demo):');
        console.log(`   Error: ${error.message}`);
        
        if (error.code === 'INSUFFICIENT_FUNDS') {
          console.log('   üí° This usually means the demo account has low balance');
        }
      }
    } else {
      console.log('‚ÑπÔ∏è  No accounts or beneficiaries available for transfer simulation');
    }
    
    // =========================================================================
    // 7. TRANSFER HISTORY
    // =========================================================================
    
    console.log('\nüìã Loading transfer history...');
    
    try {
      const transferHistory = await sdk.transfers.getHistory({
        limit: 5
      });
      
      console.log(`\nüìä Recent Transfers (${transferHistory.length}):`);
      transferHistory.forEach(transfer => {
        console.log(`  üîÑ ${transfer.reference} - ${transfer.status}`);
        console.log(`    üíµ Amount: $${transfer.amountSent.toFixed(2)}`);
        console.log(`    üìÖ Date: ${transfer.createdAt.toLocaleDateString()}`);
        console.log(`    üë§ To: ${transfer.recipient.name || 'Unknown'}`);
        console.log(`    üìù Description: ${transfer.description}`);
        console.log('');
      });
    } catch (error) {
      console.log('‚ÑπÔ∏è  Transfer history not available (this is normal for new accounts)');
    }
    
    // =========================================================================
    // 8. SDK CONFIGURATION AND STATUS
    // =========================================================================
    
    console.log('\n‚öôÔ∏è  SDK Configuration:');
    const config = sdk.getConfig();
    console.log(`  üåç Environment: ${config.environment}`);
    console.log(`  ‚è±Ô∏è  Timeout: ${config.timeout}ms`);
    console.log(`  üêõ Debug: ${config.debug ? 'Enabled' : 'Disabled'}`);
    console.log(`  üîÑ Auto Refresh: ${config.retries ? 'Enabled' : 'Disabled'}`);
    
    console.log('\nüîê Authentication Status:');
    console.log(`  ‚úÖ Authenticated: ${sdk.isAuthenticated()}`);
    console.log(`  üé´ Token Valid Until: ${sdk.getTokenExpiry()?.toLocaleString()}`);
    
    const currentProfile = sdk.getUserProfile();
    if (currentProfile) {
      console.log(`  üë§ Current User: ${currentProfile.email}`);
    }
    
    console.log('\n‚úÖ Basic usage example completed successfully!');
    console.log('\nüí° Next steps:');
    console.log('  - Check out wallet-app.js for a complete wallet implementation');
    console.log('  - See transfers.js for advanced transfer workflows');
    console.log('  - Read the documentation at https://developer.tropipay.com');
    
  } catch (error) {
    console.error('\n‚ùå Example failed:', error.message);
    
    if (error.code === 'AUTHENTICATION_FAILED') {
      console.error('\nüí° Make sure to set correct environment variables:');
      console.error('   TROPIPAY_CLIENT_ID=your_client_id');
      console.error('   TROPIPAY_CLIENT_SECRET=your_client_secret');
    }
    
    console.error('\nüêõ Full error details:', error);
  }
}

// Run the example if this file is executed directly
if (require.main === module) {
  basicUsageExample()
    .then(() => {
      console.log('\nüëã Example finished. Goodbye!');
      process.exit(0);
    })
    .catch((error) => {
      console.error('\nüí• Unhandled error:', error);
      process.exit(1);
    });
}

module.exports = basicUsageExample;